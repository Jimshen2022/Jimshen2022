FROM LLUSAF.WVCNTSD a 
WHERE a.WCSITEMNUMBER LIKE '%UN' AND a.WCSITEMNUMBER NOT LIKE 'M%' 

INNER JOIN 

SELECT b.WCHCONTAINERNUMBER,b.WCHCONTAINERSTATUS
FROM LLUSAF.WVCNTHD b 
WHERE b.WCHCONTAINERSTATUS IN ('P','T') AND b.WCHPOSTEDTIMESTAMP BETWEEN CHAR(current DATE - 3 days) AND CHAR(current DATE) 

---- CURRENT CONTAINER HEAD TABLE FOR  LLUSAF.WVCNTHD-Head
ON a.WCSCONTAINERNUMBER = b.WCHCONTAINERNUMBER








